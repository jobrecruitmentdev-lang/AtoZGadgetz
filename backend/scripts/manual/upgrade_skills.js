function processSkillDir(skillsDir) {
  if (!fs.existsSync(skillsDir)) return;
  const items = fs.readdirSync(skillsDir);
  for (const item of items) {
    const itemPath = path.join(skillsDir, item);
    if (fs.statSync(itemPath).isDirectory()) {
      const skillMdPath = path.join(itemPath, 'SKILL.md');
      if (fs.existsSync(skillMdPath)) {
        let content = fs.readFileSync(skillMdPath, 'utf8');
        
        // Remove old injection if it exists
        const oldInjectionIndex = content.indexOf('# ⚠️ CRITICAL: PRODUCTION AI ENGINEERING ENHANCEMENTS');
        if (oldInjectionIndex !== -1) {
          content = content.substring(0, oldInjectionIndex).trim();
        }
        
        // Remove new injection if it exists (for idempotency)
        const newInjectionIndex = content.indexOf('# ⚠️ CRITICAL: PRODUCTION WEB ENGINEER ENHANCEMENTS');
        if (newInjectionIndex !== -1) {
          content = content.substring(0, newInjectionIndex).trim();
        }

        content += '\n\n' + masterRules.trim() + '\n';
        fs.writeFileSync(skillMdPath, content, 'utf8');
        console.log(`Upgraded: ${skillMdPath}`);
      }
    }
  }
}

for (const dir of targetDirs) {
  console.log(`Processing directory: ${dir}`);
  processSkillDir(dir);
}
